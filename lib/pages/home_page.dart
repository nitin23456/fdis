
import 'package:FdisTesting/pages/dashboard_page.dart';
import 'package:FdisTesting/pages/info_page.dart';
import 'package:FdisTesting/pages/setting_page.dart';
import 'package:FdisTesting/widgets/bottom_nav_bar.dart';
import 'package:flutter/material.dart';

class HomePage extends StatelessWidget {
  final _tab1navigatorKey = GlobalKey<NavigatorState>();
  final _tab2navigatorKey = GlobalKey<NavigatorState>();
  final _tab3navigatorKey = GlobalKey<NavigatorState>();

  @override
  Widget build(BuildContext context) {
    return PersistentBottomBarScaffold(
      items: [
        PersistentTabItem(
          tab:  DashboardPage(),
          icon: Icons.file_copy_outlined,
          title: 'Audits',
          navigatorkey: _tab1navigatorKey,
        ),
        PersistentTabItem(
          tab:  SettingPage(),
          icon: Icons.settings,
          title: 'Settings',
          navigatorkey: _tab2navigatorKey,
        ),
        PersistentTabItem(
          tab:  InfoPage(),
          icon: Icons.info_outline,
          title: 'Help',
          navigatorkey: _tab3navigatorKey,
        ),
      ],

    );
  }
}



class Page1 extends StatelessWidget {
  final String inTab;

  const Page1(this.inTab);

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text('Page 1')),
      body: SizedBox(
        width: double.infinity,
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            Text('in $inTab Page 1'),
            ElevatedButton(
                onPressed: () {
                  Navigator.of(context).push(
                      MaterialPageRoute(builder: (context) => Page2(inTab)));
                },
                child: const Text('Go to page2'))
          ],
        ),
      ),
    );
  }
}

